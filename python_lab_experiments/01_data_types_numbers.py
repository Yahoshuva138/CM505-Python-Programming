# Program 1: Working with Numbers
# Demonstrating integer and float data types

print("=" * 50)
print("Program 1: Working with Numbers")
print("=" * 50)

# Integer operations
num1 = 10
num2 = 20
print(f"\nInteger Operations:")
print(f"num1 = {num1}")
print(f"num2 = {num2}")
print(f"Addition: {num1} + {num2} = {num1 + num2}")
print(f"Subtraction: {num1} - {num2} = {num1 - num2}")
print(f"Multiplication: {num1} * {num2} = {num1 * num2}")
print(f"Division: {num1} / {num2} = {num1 / num2}")
print(f"Floor Division: {num1} // {num2} = {num1 // num2}")
print(f"Modulus: {num1} % {num2} = {num1 % num2}")
print(f"Power: {num1} ** 2 = {num1 ** 2}")

# Float operations
fnum1 = 15.5
fnum2 = 3.5
print(f"\nFloat Operations:")
print(f"fnum1 = {fnum1}")
print(f"fnum2 = {fnum2}")
print(f"Addition: {fnum1} + {fnum2} = {fnum1 + fnum2}")
print(f"Subtraction: {fnum1} - {fnum2} = {fnum1 - fnum2}")
print(f"Multiplication: {fnum1} * {fnum2} = {fnum1 * fnum2}")
print(f"Division: {fnum1} / {fnum2} = {fnum1 / fnum2}")

# Data type checking
print(f"\nData Type Information:")
print(f"Type of {num1}: {type(num1)}")
print(f"Type of {fnum1}: {type(fnum1)}")
